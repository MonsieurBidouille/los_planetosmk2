package main;

public class Speed {
    double Xspeed;
    double Yspeed;

    public Speed(double xspeed, double yspeed) {
        Xspeed = xspeed;
        Yspeed = yspeed;
    }

    public double getXspeed() {
        return Xspeed;
    }

    public void setXspeed(double xspeed) {
        Xspeed = xspeed;
    }

    public double getYspeed() {
        return Yspeed;
    }

    public void setYspeed(double yspeed) {
        Yspeed = yspeed;
    }
}
